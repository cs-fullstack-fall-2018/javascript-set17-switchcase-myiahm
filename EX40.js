function main()
{
    var userInput = parseInt(prompt("Enter a number: "));
switch (userInput)
{
    case (1):
        alert("1");
        break;
    case (10):
        alert("2");
        break;
    case (11):
        alert("3");
        break;
    case (100):
        alert("4");
        break;
    case (101):
        alert("5");
        break;
    default:
        alert("Error!!!");
        break;
}
}